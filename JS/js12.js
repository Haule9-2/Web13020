let ctx = c.getContext("2d");
ctx.beginPath();
let grdcircle = ctx.createRadialGradient(150, 150, 140, 150, 150, 150);
grdcircle.addColorStop(0, "red");
grdcircle.addColorStop(1, "cyan");
ctx.fillStyle = grdcircle;
ctx.fillRect(0, 0, 300, 300);
ctx.arc(150, 150, 150, 0, 2 * Math.PI);
ctx.stroke();
ctx.fillStyle = "yellow";
ctx.beginPath();
ctx.rotate((45 * Math.PI) / 180);
ctx.fillRect(109, -108, 200, 210);
ctx.stroke();
ctx.rotate((-45 * Math.PI) / 180);
ctx.strokeStyle = "blue";
